    private void addDeficitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDeficitActionPerformed
        // TODO add your handling code here:
        String deficitID = null;
        String memoryLoss;
        String patientID;
        String name;
        String surname;
        String ageStr;
        int age = -1;
        String gender;
        String affectedArea;
        String type;
        String cause;
        String brlocation;
        try {
            deficitID = deficidID.getText();
            //IDcomboBox.addItem(deficitID);
            memoryLoss = memoryComboBox.getSelectedItem().toString();
            patientID = this.patientID.getText();
            name = this.name.getText();
            surname = this.surname.getText();
            ageStr = this.age.getText();
            age = Integer.parseInt(ageStr);
            gender = genderComboBox.getSelectedItem().toString();
            if (alzheimer.isSelected()) {
                affectedArea = areaComboBox.getSelectedItem().toString();
                Alzheimer alzh = new Alzheimer(deficitID, memoryLoss, affectedArea, patientID, name,
                        surname, age, gender);
                InfoClass.addMemory(alzh);
                IDcomboBox.addItem(deficitID);
                info1.append("\nAlzheimer is added");
            }
            if (amnesia.isSelected()) {
                type = typeComboBox.getSelectedItem().toString();
                cause = causeComboBox.getSelectedItem().toString();
                brlocation = brainLocComboBox.getSelectedItem().toString();
                Amnesia ams = new Amnesia(deficitID, memoryLoss, type, cause, brlocation, patientID, name,
                        surname, age, gender);
                InfoClass.addMemory(ams);
                IDcomboBox.addItem(deficitID);
                info1.append("\nAmnesia is added");

            }

            deficidID.setText(null);
            this.name.setText(null);
            this.surname.setText(null);
            this.age.setText(null);
            this.patientID.setText(null);

            genderComboBox.setSelectedIndex(-1);
            typeComboBox.setSelectedIndex(-1);
            causeComboBox.setSelectedIndex(-1);
            brainLocComboBox.setSelectedIndex(-1);
            areaComboBox.setSelectedIndex(-1);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "All fields must be filled!", "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_addDeficitActionPerformed

