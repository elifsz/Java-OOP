    private void totalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalActionPerformed
        // TODO add your handling code here:
        int totalAll = MemoryDeficit.getTotalDeficit();
        int totalAlz = Alzheimer.getTotalAlzheimer();
        int totalAmn = Amnesia.getTotalAmnesia();
        if (InfoClass.memoryArrayList.isEmpty()) {
            info2.append("no items");
        } else {
            info2.setText("Total Deficit: " + totalAll
                    + "\nTotal Alzheimer: " + totalAlz
                    + "\nTotal Amnesia: " + totalAmn + "\n");
        }
    }//GEN-LAST:event_totalActionPerformed

    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        info3.setText(null);
        if (InfoClass.memoryArrayList.isEmpty()) {
            info3.append("no items");
        } else {
            String selectedID = IDcomboBox.getSelectedItem().toString();
            MemoryDeficit find = InfoClass.searchMemory(selectedID);
            if (find == null) {
                info3.append(selectedID + " not found");
            } else {
                info3.setText(find.toString());
            }

        }
    }//GEN-LAST:event_searchActionPerformed

